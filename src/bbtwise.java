public class bbtwise {
    public static void main(String[] args) {
        int p=9;
        int q=10;
        // bitwise operator
        System.out.println(p|q);
        System.out.println(p&q);
        System.out.println(p^q);
        //shift operator
        System.out.println(p<<1);
        System.out.println(p>>1);

    }
}
